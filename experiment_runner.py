"""Experiment runner for implementing research ideas"""

import json
from pathlib import Path
from datetime import datetime
import subprocess
import os

DATA_DIR = Path.home() / '.openclaw' / 'workspace' / 'research-system' / 'data'
EXPERIMENTS_DIR = Path.home() / '.openclaw' / 'workspace' / 'research-system' / 'experiments'
EXPERIMENTS_DIR.mkdir(parents=True, exist_ok=True)

class ExperimentRunner:
    """Run experiments to test research ideas"""

    def __init__(self):
        self.experiments_file = DATA_DIR / 'experiments.json'

    def create_experiment(self, idea: dict) -> str:
        """Create a new experiment directory and template"""
        exp_id = datetime.now().strftime('%Y%m%d_%H%M%S')
        exp_dir = EXPERIMENTS_DIR / exp_id
        exp_dir.mkdir(exist_ok=True)

        direction = idea.get('direction', 'Untitled')
        gap = idea.get('gap', '')
        idea_text = idea.get('idea', '')
        novelty = idea.get('novelty', 'medium')

        # Create README
        readme = f"""# Experiment: {direction}

**Experiment ID:** {exp_id}
**Created:** {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}
**Novelty:** {novelty}

## Research Gap

{gap}

## Proposed Idea

{idea_text}

## Implementation Plan

- [ ] Step 1: Literature review and baseline setup
- [ ] Step 2: Core algorithm implementation
- [ ] Step 3: Experimental setup
- [ ] Step 4: Data collection
- [ ] Step 5: Analysis and results

## Results

*Results will be added here after implementation*

## Next Steps

*Action items for further development*

---

Generated by: quack quack 🦆
"""
        (exp_dir / 'README.md').write_text(readme)

        # Create implementation file
        impl_file = exp_dir / 'implementation.py'
        impl_file.write_text('"""Implementation file for this experiment"""\n\n# TODO: Implement the proposed idea\n')

        # Log experiment
        self.log_experiment(exp_id, idea, exp_dir)

        return str(exp_dir)

    def log_experiment(self, exp_id: str, idea: dict, exp_dir: Path) -> None:
        """Log experiment to central file"""
        experiments = []

        if self.experiments_file.exists():
            with open(self.experiments_file) as f:
                experiments = json.load(f)

        experiment_entry = {
            'id': exp_id,
            'direction': idea.get('direction', ''),
            'created': datetime.now().isoformat(),
            'status': 'pending',
            'novelty': idea.get('novelty', 'medium'),
            'directory': str(exp_dir)
        }

        experiments.append(experiment_entry)

        with open(self.experiments_file, 'w') as f:
            json.dump(experiments, f, indent=2)

    def update_experiment_status(self, exp_id: str, status: str, results: dict = None) -> None:
        """Update experiment status and add results"""
        experiments = []

        if self.experiments_file.exists():
            with open(self.experiments_file) as f:
                experiments = json.load(f)

        # Find and update experiment
        for exp in experiments:
            if exp['id'] == exp_id:
                exp['status'] = status
                exp['updated'] = datetime.now().isoformat()
                if results:
                    exp['results'] = results
                break

        with open(self.experiments_file, 'w') as f:
            json.dump(experiments, f, indent=2)

        # Also update README in experiment directory
        exp_dir = Path(exp['directory'])
        if exp_dir.exists():
            readme = exp_dir / 'README.md'
            content = readme.read_text()

            # Add results section if not exists
            if '## Results' not in content:
                content += '\n\n## Results\n\n*Results will be added here after implementation*\n'
                readme.write_text(content)

    def get_pending_experiments(self) -> list:
        """Get list of pending experiments"""
        if not self.experiments_file.exists():
            return []

        with open(self.experiments_file) as f:
            experiments = json.load(f)

        return [e for e in experiments if e['status'] == 'pending']

if __name__ == '__main__':
    import sys

    if len(sys.argv) < 2:
        print("Usage: experiment_runner.py <command> [args]")
        print("Commands:")
        print("  create <idea_json> - Create new experiment")
        print("  status <exp_id> <status> [results_json] - Update experiment status")
        print("  pending - List pending experiments")
        sys.exit(1)

    runner = ExperimentRunner()
    command = sys.argv[1]

    if command == 'create':
        if len(sys.argv) < 3:
            print("Error: idea JSON required")
            sys.exit(1)

        idea = json.loads(sys.argv[2])
        exp_dir = runner.create_experiment(idea)
        print(f"✓ Experiment created: {exp_dir}")

    elif command == 'status':
        if len(sys.argv) < 4:
            print("Error: exp_id and status required")
            sys.exit(1)

        exp_id = sys.argv[2]
        status = sys.argv[3]
        results = json.loads(sys.argv[4]) if len(sys.argv) > 4 else None

        runner.update_experiment_status(exp_id, status, results)
        print(f"✓ Experiment {exp_id} updated to {status}")

    elif command == 'pending':
        pending = runner.get_pending_experiments()
        print(f"Pending experiments: {len(pending)}")
        for exp in pending:
            print(f"  - {exp['id']}: {exp['direction']}")
